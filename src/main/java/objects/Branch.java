package objects;

public class Branch {
    private int id;
    private String name;
    private String address;

    public Branch(){
        id = 0;
        name = "-";
        address = "-";
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getAddress() {
        return address;
    }
}
